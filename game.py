class Game:
    def __init__(self):
        # Window
        self.window = pygame.display.set_mode(
            (SCREEN_WIDTH, SCREEN_HEIGHT),
            pygame.RESIZABLE
        )
        pygame.display.set_caption(TITLE)

        self.screen = pygame.Surface((SCREEN_WIDTH, SCREEN_HEIGHT))
        self.window_size = self.window.get_size()

        self.clock = pygame.time.Clock()
        self.running = True

        # World
        self.tilemap = TileMap(level)

        # Entities
        self.player = Player(tile_pos=(1, 1))
        self.enemies = []
        self.troops = []
        self.projectiles = []

        # Wave system
        self.wave_manager = WaveManager(self.tilemap)
        self.wave_manager.start_wave(enemy_count=15)

        # Shop system
        self.shop = Shop(SCREEN_WIDTH, SCREEN_HEIGHT)
        self.shopkeeper = Shopkeeper(tile_pos=(12, 7), tile_size=TILE_SIZE)

        # --- TEST TROOPS ---
        self.troops.append(Troop(tile_pos=(25, 7)))
        self.troops.append(Troop(tile_pos=(28, 5)))
        self.troops.append(Troop(tile_pos=(34, 3)))
        self.troops.append(Troop(tile_pos=(25, 2)))

    # -----------------------------
    # Utility
    # -----------------------------
    def screen_to_tile(self, mouse_pos):
        mx, my = mouse_pos
        window_w, window_h = self.window.get_size()

        scale = min(
            window_w / SCREEN_WIDTH,
            window_h / SCREEN_HEIGHT
        )

        scaled_w = SCREEN_WIDTH * scale
        scaled_h = SCREEN_HEIGHT * scale

        x_offset = (window_w - scaled_w) / 2
        y_offset = (window_h - scaled_h) / 2

        # Ignore clicks outside game area
        if (
            mx < x_offset or
            my < y_offset or
            mx > x_offset + scaled_w or
            my > y_offset + scaled_h
        ):
            return None

        # Convert back to logical coordinates
        lx = (mx - x_offset) / scale
        ly = (my - y_offset) / scale

        tile_x = int(lx // TILE_SIZE)
        tile_y = int(ly // TILE_SIZE)

        return tile_x, tile_y

    # -----------------------------
    # Events
    # -----------------------------
    def handle_events(self):
        for event in pygame.event.get():
            if event.type == pygame.QUIT:
                self.running = False

            elif event.type == pygame.VIDEORESIZE:
                self.window = pygame.display.set_mode(
                    (event.w, event.h),
                    pygame.RESIZABLE
                )
                self.window_size = (event.w, event.h)

            elif event.type == pygame.KEYDOWN:
                if event.key == pygame.K_e:
                    if self.shopkeeper.is_player_close(self.player):
                        self.shop.toggle()
                    elif self.shop.active:
                        self.shop.toggle()

            elif event.type == pygame.MOUSEBUTTONDOWN:
                if event.button == 1 and not self.shop.active:
                    tile = self.screen_to_tile(event.pos)
                    if tile:
                        tile_x, tile_y = tile
                        if self.tilemap.is_buildable(tile_x, tile_y):
                            self.troops.append(Troop(tile_pos=(tile_x, tile_y)))


    # -----------------------------
    # Update
    # -----------------------------
    def update(self, dt):
        if self.shop.active:
            self.shop.handle_input(self.player)
            return  # pause game logic

        self.player.update(dt, self.tilemap)

        # Enemies
        self.wave_manager.update(dt, self.enemies)
        for enemy in self.enemies:
            enemy.update(dt)

        # Troops
        for troop in self.troops:
            troop.update(dt, self.enemies, self.projectiles)

        # Projectiles
        for projectile in self.projectiles:
            projectile.update(dt)

        # Cleanup
        self.projectiles = [p for p in self.projectiles if p.alive]
        self.enemies = [
            e for e in self.enemies
            if not e.finished and e.health > 0
        ]

    # -----------------------------
    # Draw
    # -----------------------------
    def draw(self):
        self.screen.fill(BG_COLOR)

        self.tilemap.draw(self.screen)
        self.player.draw(self.screen)

        for troop in self.troops:
            troop.draw(self.screen)

        for enemy in self.enemies:
            enemy.draw(self.screen)

        for projectile in self.projectiles:
            projectile.draw(self.screen)

        self.shopkeeper.draw(self.screen, self.player)

        if self.shop.active:
            self.shop.draw(self.screen, self.player)

        # ===== PROPER SCALING =====
        window_w, window_h = self.window.get_size()
        scale = min(
            window_w / SCREEN_WIDTH,
            window_h / SCREEN_HEIGHT
        )

        scaled_w = int(SCREEN_WIDTH * scale)
        scaled_h = int(SCREEN_HEIGHT * scale)

        scaled_surface = pygame.transform.scale(
            self.screen,
            (scaled_w, scaled_h)
        )

        # Center the game (letterboxing)
        x_offset = (window_w - scaled_w) // 2
        y_offset = (window_h - scaled_h) // 2

        self.window.fill(BLACK)
        self.window.blit(scaled_surface, (x_offset, y_offset))

        pygame.display.flip()

    # -----------------------------
    # Main Loop
    # -----------------------------
    def run(self):
        while self.running:
            dt = self.clock.tick(FPS) / 1000
            self.handle_events()
            self.update(dt)
            self.draw()
